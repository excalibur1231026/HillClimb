RoadManager::RoadManager() {
    
  //  childSprites = new cocos2d::Vector<Sprite *>();
    DoodleTruck *doodleTruck = DoodleTruck::sharedDoodleTruck();
    format_string = "ipad_road_%d_%02d.png";//"road_%d_%02d.png";
        
    road_count = 15;
    
    cocos2d::Size s = Director::getInstance()->getWinSize();
    float r_scaleX = 1024.0f / s.width;
    float r_scaleY = 768.0f / s.height;
    //if (doodleTruck->getScaleX() == 1.0f) {
        r_scaleX = 1024.0f/480.0f;
        r_scaleY = 768.0f/320.0f;
    //}*/
        
    road_sprite = (Sprite **)calloc(road_count, sizeof(Sprite *));
    for (int i = 0; i < road_count; i++)
    {
        char str[100];
        sprintf(str, format_string.c_str(), getImageNum(), i);
        std::string str_key = str;
        

        road_sprite[i] = Sprite::create(str_key);
        
        if (r_scaleX * r_scaleY != 1.0f) {
            road_sprite[i]->setScaleX(r_scaleX);
            road_sprite[i]->setScaleY(r_scaleY);
        }
    }
    
    road_height = road_sprite[0]->getContentSize().height * r_scaleY;
    road_width = road_sprite[0]->getContentSize().width * r_scaleX;
    total_point = road_count * road_sprite[0]->getContentSize().width * r_scaleX;
    
    initPoint();
}

int RoadManager::getImageNum() {
    
    DoodleTruck * doodleTruck = DoodleTruck::sharedDoodleTruck();
	int image_num = doodleTruck->getMapIndex() + 1;
	if (image_num == 5) 
		image_num = 10;
	if (image_num == 13) 
		image_num = 8;
	return image_num;
}

void RoadManager::addVisibleRoad(Layer *parent_layer) {
    
	for (int i = 0; i < road_count; i++)
	{
		double pos_y = road_height / 2.0f - (beginY - minY);
        road_sprite[i]->setPosition( Vec2(road_width * (i + 0.5f), pos_y));
        parent_layer->addChild(road_sprite[i], 5);
	}
}


